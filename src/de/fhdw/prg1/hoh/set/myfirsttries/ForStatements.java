package de.fhdw.prg1.hoh.set.myfirsttries;

public class ForStatements {
    public static void main(String[] args) {
        int i;
        for (i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        i = 0;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }

    }
}
